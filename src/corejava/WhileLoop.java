/**
 * Write a program to demonstrate the use of while loop. 
 * (E.g print 1 to 10 numbers).
 * @author Darshit Patel
 * @version 1.01 2022-09-07
 */

public class WhileLoop {

    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }

}
